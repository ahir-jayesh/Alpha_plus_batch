public class Assignment_Oprator {
    public static void main(String[] args) {
        /*
         * Assgnment Oprator 
         * 1. =     a=b
         * 2. +=    a=a+b
         * 3. -=    a=a-b
         * 4. *=    a=a*b
         * 5. /=    a=a/b
         * 6. %=    a=a%b
         */
        int a=10;
        int b=20;
        a+=b;
        System.out.println(a);
    }
}
