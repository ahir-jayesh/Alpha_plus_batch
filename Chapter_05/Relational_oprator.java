public class Relational_oprator {
    public static void main(String[] args) {
        /*
         * Relational Oprator
         * 1. equal to ==
         * 2. Not equal to !=
         * 3. less than <
         * 4. Greter Then >
         * 5. Less then or Equal to
         * 6. Greater Then or equal to
         */
        int a = 10, b = 12, c = 10;
        if (a == c)
            System.out.println("Equal both");
        if (a != b)
            System.out.println("Not-equal both");
        if (a<b)
            System.out.println("less then");
        if (b>a)
            System.out.println("Graterthen both");
        if (a <= c)
            System.out.println("less or Equal both");
        if (a >= c)
            System.out.println("greater or Equal both");
    }
}
