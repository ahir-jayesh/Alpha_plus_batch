class Java_variable {
    public static void main(String[] args) {
        byte a = 127;
        short b = 1234;
        int c = 12345;
        float d = 123456.7f;
        double e = 12345678.9;
        char f = 'j';
        long g = 12313413423434421l;
        boolean h = true;
        System.out.println(a);     //byte variable
        System.out.println(b);     // int variable
        System.out.println(c);     // so on....
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        //variable are change in run time 
        // this is all Primitive Data type 
        // java is a type language because it is use first data type declaration
    }
}