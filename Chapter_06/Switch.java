public class Switch {
    public static void main(String[] args) {
        int age=18;
        switch(age){
            case 1:
            System.out.println("Age is 1");
            break;
            case 2:
            System.out.println("Age is 2");
            break;
            default :
            System.out.println("Age is Default");
        }
    }
}
